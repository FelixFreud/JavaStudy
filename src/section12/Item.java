package section12;

/**Q12-1*/
public class Item {
	private String name;
	private int price;
	private int whight;
	public String getName() {
		return this.name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return this.price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getWhight() {
		return this.whight;
	}
	public void setWhight(int whight) {
		this.whight = whight;
	}
}
